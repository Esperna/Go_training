// Mandelbrot emits a PNG image of the Mandelbrot fractal.
package main

import (
	"image"
	"image/color"
	"image/png"
	"io"
	"log"
	"math/cmplx"
	"net/http"
	"os"
	"strconv"
	"strings"
)

var x = 0
var y = 0
var ratio = 1

func main() {
	if len(os.Args) > 1 && os.Args[1] == "web" {
		handler := func(w http.ResponseWriter, r *http.Request) {
			r.ParseForm()
			for k, v := range r.Form {
				if k == "x" {
					x, _ = strconv.Atoi(strings.Join(v, ""))
				} else if k == "y" {
					y, _ = strconv.Atoi(strings.Join(v, ""))
				} else if k == "ratio" {
					ratio, _ = strconv.Atoi(strings.Join(v, ""))
				}
			}
			drawMandelbrot(w)
		}
		http.HandleFunc("/", handler)
		//!-http
		log.Fatal(http.ListenAndServe("localhost:8000", nil))
		return
	}
	drawMandelbrot(os.Stdout)
	log.Fatal(http.ListenAndServe("localhost:8000", nil))
}

func drawMandelbrot(out io.Writer) {
	const (
		xmin, ymin, xmax, ymax = -2, -2, +2, +2
		width, height          = 1024, 1024
	)

	img := image.NewRGBA(image.Rect(0, 0, width, height))
	for py := 0; py < height; py++ {
		_y := (float64(py)/height*(ymax-ymin) + ymin) / float64(ratio)
		for px := 0; px < width; px++ {
			_x := (float64(px)/width*(xmax-xmin) + xmin) / float64(ratio)
			z := complex(_x, _y)
			// Image point (px, py) represents complex value z.
			img.Set(x+px, y+py, mandelbrot(z))
		}
	}
	png.Encode(out, img) // NOTE: ignoring errors
}

func mandelbrot(z complex128) color.Color {
	const iterations = 200
	const contrast = 15

	var v complex128
	for n := uint8(0); n < iterations; n++ {
		v = v*v + z
		if cmplx.Abs(v) > 2 {
			return sqrt(z)
			//return color.Gray{255 - contrast*n}
		}
	}
	return acos(z)
}

